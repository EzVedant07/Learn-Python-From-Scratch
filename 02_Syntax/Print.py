# Example of using the print() function

# Printing a simple string
print("Hello, World!")

# Printing multiple values
name = "vedant"
age =19
print("Name:", name, "Age:", age)

# Printing numbers and expressions
print("The result of 5 + 3 is:", 5 + 3)

# Using formatted strings (f-strings)
print(f"{name} is {age} years old.")

# Printing with special characters
print("This is a new line\nAnd this is a tab:\tIndented text")